void showCascading(std::string s, time_t t, unsigned short int xStart, unsigned short int yStart){
    unsigned short int lineFeed = yStart;
    gotoxy(xStart, lineFeed);
    for (int i = 0; i < s.length(); ++i) {
        if(s[i] == '\n'){
            lineFeed++;
            gotoxy(xStart, lineFeed);
            continue;
        }
        std::cout << s[i];
        Sleep(t);
    }

}

